import math

print(round(1.3))  # número entero más cercano
print(round(1.7))
print(round(1.5))
print(abs(-77))  # valor absoluto de un número
print(abs(55))

print(math.ceil(1.1))  # número entero grande más cercano
print(math.floor(1.999))  # número entero chico más cercano
print(math.isnan(23))  # nos va a informar si le estamos pasando un número real
# print(math.isnan("23"))
print(math.pow(10, 3))  # elevamos 10 a la 3
print(math.sqrt(9))  # la raíz cuadrada de un número

